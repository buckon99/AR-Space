package com.csc309.arspace.models;

public class Product {

    private String title;
    private String type;
    private int width;
    private int height;
    private int length;

    public Product(String title, String type, int width, int height, int length) {
        this.title = title;
        this.type = type;
        this.width = width;
        this.height = height;
        this.length = length;
    }

}
